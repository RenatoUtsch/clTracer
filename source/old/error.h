#ifndef ERROR_H
#define ERROR_H

#include <stdio.h>
#include <stdlib.h>

/**
 * Stops the program if the condition given is false.
 * You can write an error message in the same style as printf(), but to stderr.
 */
#define stop_if(cond, ...) do { if(cond) { fprintf(stderr, "%s:%d:%s: ", __FILE__, __LINE__, __func__); fprintf(stderr, __VA_ARGS__); fprintf(stderr, "\n"); exit(1); } } while(0)

#endif // !ERROR_H
